let currentLang = localStorage.getItem('doro-lang') || 'ko';

function applyLanguage(lang) {
  currentLang = lang;
  localStorage.setItem('doro-lang', lang);

  document.querySelectorAll('[data-i18n]').forEach((element) => {
    const key = element.getAttribute('data-i18n');
    const text = i18n[lang][key];
    if (text) {
      element.textContent = text;
    }
  });

  const toggleButton = document.getElementById('lang-toggle');
  if (toggleButton) {
    toggleButton.textContent = i18n[lang]['lang.toggle'];
    toggleButton.setAttribute('title', lang === 'ko' ? 'Switch to English' : '한국어로 전환');
  }

  document.documentElement.lang = lang === 'ko' ? 'ko' : 'en';
}

function toggleLanguage() {
  applyLanguage(currentLang === 'ko' ? 'en' : 'ko');
}

document.addEventListener('DOMContentLoaded', () => {
  applyLanguage(currentLang);
});
